#include "libft.h"

void	ft_putnbr_fd(int n, int fd)
{
	char	*s;

	s = "0123456789";
	if (n == -2147483648)
	{
		write(fd, "-2147483648", 11);
		return ;
	}
	if ((n > 2147483647) && (n < -2147483647))
	{
		return ;
	}
	if (n < 0)
	{
		write(fd, "-", 1);
		n = -n;
	}
	if ((n <= 2147483647) && (n >= 10))
		ft_putnbr_fd (n / 10, fd);
	write(fd, &s[n % 10], 1);
}
